// while loop

let index = 0;
while (index <= 10) {
    console.log(`Value of i : ${index}`);
    index++
}


let array = ['Captain America', 'Iron Man', 'Thor', 'Hulk', 'Loki']

let i = 0;
while (i < array.length) {
    console.log(`Value is : ${array[i]}`);
    i++;
}


// do while loop

let score = 1
do {
    console.log(`Score is : ${score}`);
    score++;
} while (score <= 10);
